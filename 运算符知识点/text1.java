public class text1 {
	/**
	 * 6种关系运算符
	 *   ==  !=  >  <  <=  >=
	 */
	public static void main(String[] args){
      
      int a=10;
      int b=20;
      System.out.println(a==b);
      System.out.println(a!=b);
      System.out.println(a<b);
      System.out.println(a>b);
      System.out.println(a<=b);
      System.out.println(a>=b);
		//关系运算符的返回值都是boolean
	}
}
